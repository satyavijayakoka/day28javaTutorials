package javaTutorials;

public class dayOne {

	public static void main(String[] args) {
		
		System.out.println("HELLO");
		System.out.println("Bye");
		
		
		int x = 10;
		int y = 5;
		
		//Addition
		System.out.println(x+y);
		
		//Subtraction
		System.out.println(x-y);
		
		//Multiplication
		System.out.println(x*y);
		
		//Division
		System.out.println(x/y);
		
		//Modulus
		System.out.println(x%y);
		
		
		int p = 12;
		int q = 5;
		
		//Addition
		System.out.println(p+q);
		
		//Subtraction
		System.out.println(p-q);
		
		//Multiplication
		System.out.println(p*q);
		
		//Division
		System.out.println(p/q);
		
		//Modulus
		System.out.println(p%q);
		
	}

}
